// 3581 - Count Odd Letters From Number

function countOddLetters(n) {
    const dict = [
        () => ++B[4] && ++B[14] && ++B[17] && ++B[25],
        () => ++B[4] && ++B[13] && ++B[14],
        () => ++B[14] && ++B[19] && ++B[22],
        () => ++B[4] && ++B[4] && ++B[7] && ++B[17] && ++B[19],
        () => ++B[5] && ++B[14] && ++B[17] && ++B[20],
        () => ++B[4] && ++B[5] && ++B[8] && ++B[21],
        () => ++B[8] && ++B[18] && ++B[23],
        () => ++B[4] && ++B[4] && ++B[13] && ++B[18] && ++B[21],
        () => ++B[4] && ++B[6] && ++B[7] && ++B[8] && ++B[19],
        () => ++B[4] && ++B[8] && ++B[13] && ++B[13],
    ];
    const B = new Uint8Array(26);
    while (n) dict[n % 10]() && (n = n / 10 >> 0);
    return B.reduce((s, e) => s + e % 2, 0);
}
